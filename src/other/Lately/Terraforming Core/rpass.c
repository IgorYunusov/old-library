unsigned char **dec_h(unsigned char **s, int strsize)
	{ // decryption hex
	int i, f;
	for(f = 0; f <= strsize; f++)
		for(i = 0; i <= 15; i++)
			s[f][i] = get_d(s[f][i]);
	return s;
	}

unsigned char **dec_k(unsigned char ** s, int strsize, unsigned char * key, int keysize)
	{ // decryption with key
	int i, f, g;
	for(f = 0; f <= strsize; f++)
		for(i = 0; i <= keysize; i++)
			for(g = 0; g <= 15; g++)
				s[f][g] ^= key[i];
	return s;
	}

unsigned char dec_c(unsigned char *s)
	{ // decryption character
	unsigned char ret[4][4], buf;
	int i, f, z = 0;
	for(i = 0; i < 4; i++)
		for(f = 0; f < 4; f++, z++)
			ret[i][f] = s[z];
	buf = ret[1][3];
	for(f = 3; f > 0; f--)
		ret[1][f] = ret[1][f - 1];
	ret[1][0] = buf;
	buf = ret[2][3];
	for(f = 3; f > 0; f--)
		ret[2][f] = ret[2][f - 1];
	ret[2][0] = buf;
	buf = ret[2][3];
	for(f = 3; f > 0; f--)
		ret[2][f] = ret[2][f - 1];
	ret[2][0] = buf;
	buf = ret[1][3];
	ret[1][3] = ret[2][2];
	ret[2][2] = buf;
	for(i = 0; i < 4; i++)
		for(f = 0; f < 4; f++)
			ret[i][f] ^= 0x30 | (0x5 - f);
	for(f = 0; f < 4; f++)
		for(i = 0, z = 3; i < 4; i++, z--)
			ret[i][f] ^= (z << 4) | (z + 0xA);
	return ret[0][3];
	}

unsigned char *Decryption(unsigned char *what, int whatsize, unsigned char *key, int keysize)
	{ // decryption main
	unsigned char **dec = 0;
	unsigned char *ret = (unsigned char *) malloc (sizeof(unsigned char) * (whatsize / 16 + 1));
	int i, z = 0;
	dec = (unsigned char **) malloc (sizeof(unsigned char *) * ( whatsize * 16 ));
	for(i = 0; i <= whatsize / 16; i++)
		dec[i] = (unsigned char *) malloc (sizeof(unsigned char) * 16);
	for(i = 0; i <= whatsize / 16; i++)
		for(int f = 0; f <= 15; f++, z++)
			dec[i][f] = what[z];
	dec_h(dec, whatsize / 16);
	dec_k(dec, whatsize / 16, key, keysize);
	for(i = 0; i <= whatsize / 16; i++)
		ret[i] = dec_c(dec[i]);
	ret[whatsize / 16] = '\0';
	return ret;
	}